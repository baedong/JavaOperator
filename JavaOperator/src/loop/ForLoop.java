public class ForLoop {
	public static void main(String[] args) {
		/*	for loop 의 장점
		 * 	1. 초기화, 한계값(limit), 증감값 설정을 한 컨디션 내부에서 일괄 설정하므로 가독성이 좋고 코딩이 빠르게 진행됨
		 */
		for (int i = 0; i < 10; i++) {
			System.out.println();
		}
	}
}
